// ComponentSpec defines the desired state of Component
type ComponentSpec struct {
	Menu CMenu `json:"menu,omitempty"`
	// the router of portal
	Portal      Router      `json:"portal,omitempty"`
	Application Application `json:"application,omitempty"`
}

type Router struct {
	// the path for request acccessing
	Path string `json:"path,omitempty"`
	// the path of the static file
	Entry string `json:"entry,omitempty"`
}

type CMenu struct {
	Name string `json:"name,omitempty"`
}

type Application struct {
	Name      string    `json:"name,omitempty"`
	HelmChart HelmChart `json:"helmChart,omitempty"`
}
type HelmChart struct {
	Name         string `json:"name,omitempty"`
	Version      string `json:"version,omitempty"`
	ValuesConfig string `json:"valuesConfig,omitempty"`
}

